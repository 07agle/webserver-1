<?php
    function sum($t1, $t2){
        $sum = $t1 + $t2;
        return $sum;
    }
    
    function multiply($t1, $t2){
        $prod = $t1 * $t2;
        return $prod;
    }

    function subtract($t1, $t2){
        $dif = $t1 - $t2;
        return $dif;
    }

    function divide($t1, $t2){
        $kvot = $t1 / $t2;
        return $kvot;
    }

